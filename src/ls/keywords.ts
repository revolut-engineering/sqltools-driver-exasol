const keywordsCompletion: { [w: string]: NSDatabase.IStaticCompletion } = keywordsArr.reduce((agg, word) => {
    agg[`"${word}"`] = {
        label: word,
        detail: word,
        filterText: word,
        sortText: (firstKeywords.includes(word) ? '2:' : '') + word,
        documentation: {
            value: `\`\`\`yaml\nWORD: ${word}\n\`\`\``,
            kind: 'markdown'
        }
    };
    return agg;
}, {});

export default keywordsCompletion;
